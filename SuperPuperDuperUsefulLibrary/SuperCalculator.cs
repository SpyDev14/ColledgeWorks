using System.Numerics;

namespace SuperPuperDuperUsefulLibrary;

/// <summary>
/// Предоставляет набор расширяющих методов для выполнения базовых арифметических операций
/// над любыми типами, поддерживающими соответствующие операторы через интерфейсы из
/// пространства имён <see cref="System.Numerics"/>.
/// </summary>
/// <remarks>
/// <para>
/// Основная цель класса — обеспечить синтаксис последовательного применения операций
/// (pipe-like syntax) для арифметических выражений. В отличие от стандартной записи
/// со скобками и учётом приоритетов операторов (* над + и т.д.), методы этого класса
/// позволяют выстраивать цепочки вычислений в естественном порядке их выполнения,
/// что улучшает читаемость кода и снижает вероятность ошибок, связанных с приоритетами.
/// </para>
/// <para>
/// Например, выражение <c>(a + b) * c</c> в стандартной нотации можно переписать как
/// <c>a.Add(b).Mul(c)</c>, что явно показывает последовательность: сначала сложение,
/// затем умножение. Аналогично, <c>a + b * c</c> (где умножение имеет higher priority)
/// может быть представлено как <c>b.Mul(c).Add(a)</c> для сохранения семантики или как
/// <c>a.Add(b).Mul(c)</c> если требуется другой порядок. Таким образом, программист
/// полностью контролирует очерёдность операций без необходимости помнить таблицу
/// приоритетов или расставлять скобки.
/// </para>
/// <para>
/// Все методы реализованы как расширяющие (extension methods), что позволяет вызывать их
/// синтаксически естественным образом: <c>a.Add(b)</c> вместо <c>SuperCalculator.Add(a, b)</c>.
/// </para>
/// <para>
/// Библиотека использует возможности современного C# с статическими абстрактными методами
/// интерфейсов (static abstract interface methods), что обеспечивает проверку типов на этапе
/// компиляции и исключает необходимость в рефлексии или боксинге значимых типов.
/// </para>
/// </remarks>
public static class SuperCalculator
{
	/// <summary>
	/// Выполняет сложение двух значений одного типа.
	/// </summary>
	/// <typeparam name="T">
	/// Тип операндов и результата. Должен реализовывать интерфейс
	/// <see cref="IAdditionOperators{T, T, T}"/>, что гарантирует наличие оператора
	/// сложения <c>+</c> для двух экземпляров типа <typeparamref name="T"/>.
	/// </typeparam>
	/// <param name="a">Первое слагаемое. Левый операнд операции сложения.</param>
	/// <param name="b">Второе слагаемое. Правый операнд операции сложения.</param>
	/// <returns>
	/// Сумма значений <paramref name="a"/> и <paramref name="b"/>.
	/// Точный результат зависит от реализации оператора <c>+</c> для типа <typeparamref name="T"/>.
	/// </returns>
	/// <exception cref="ArgumentNullException">
	/// Может быть выброшено, если тип <typeparamref name="T"/> является ссылочным типом
	/// и один из аргументов равен <c>null</c>, а реализация оператора сложения не
	/// предусматривает обработку <c>null</c>. Для значимых типов (value types) данное
	/// исключение невозможно, так как они не могут быть <c>null</c> (если только не
	/// используются обнуляемые значимые типы, но в этом случае исключение также может
	/// возникнуть при обращении к <c>Value</c>).
	/// </exception>
	/// <exception cref="OverflowException">
	/// Может возникнуть в checked-контексте (по умолчанию для проектов с включённой
	/// проверкой переполнения), если результат сложения превышает допустимый диапазон
	/// для типа <typeparamref name="T"/> (например, для целочисленных типов).
	/// В unchecked-контексте переполнение игнорируется (происходит усечение старших битов).
	/// </exception>
	/// <remarks>
	/// <para>
	/// Данный метод является обёрткой над оператором <c>+</c>. Он не добавляет никакой
	/// дополнительной логики, кроме вызова соответствующего оператора. Таким образом,
	/// поведение метода полностью определяется типом <typeparamref name="T"/>.
	/// </para>
	/// <para>
	/// Для встроенных числовых типов (int, double, decimal и т.д.) оператор сложения
	/// работает согласно спецификации языка C#. Для пользовательских типов следует
	/// обращаться к документации конкретной реализации оператора.
	/// </para>
	/// <para>
	/// Примеры использования с различными типами:
	/// <code>
	/// int sumInt = 5.Add(3);                     // 8
	/// double sumDouble = 2.5.Add(1.3);           // 3.8
	/// decimal sumDecimal = 10.5m.Add(20.3m);     // 30.8m
	/// long sumLong = 100L.Add(200L);             // 300L
	/// 
	/// // Работа с пользовательским типом, реализующим IAdditionOperators
	/// var v1 = new <see cref="Vector3"/>(1, 2, 3);
	/// var v2 = new <see cref="Vector3"/>(4, 5, 6);
	/// var sum = v1.Add(v2); // (5, 7, 9)
	/// </code>
	/// </para>
	/// <para>
	/// Благодаря синтаксису последовательного применения, можно легко комбинировать
	/// несколько операций сложения:
	/// <code>
	/// var result = a.Add(b).Add(c).Add(d); // эквивалентно a + b + c + d
	/// </code>
	/// </para>
	/// </remarks>
	public static T Add<T>(this T a, T b) where T : IAdditionOperators<T, T, T>
		=> a + b;

	/// <summary>
	/// Выполняет вычитание двух значений одного типа.
	/// </summary>
	/// <typeparam name="T">
	/// Тип операндов и результата. Должен реализовывать интерфейс
	/// <see cref="ISubtractionOperators{T, T, T}"/>, что гарантирует наличие оператора
	/// вычитания <c>-</c>.
	/// </typeparam>
	/// <param name="a">Уменьшаемое. Левый операнд операции вычитания.</param>
	/// <param name="b">Вычитаемое. Правый операнд операции вычитания.</param>
	/// <returns>
	/// Разность значений <paramref name="a"/> и <paramref name="b"/>.
	/// Результат эквивалентен выражению <c>a - b</c>.
	/// </returns>
	/// <exception cref="ArgumentNullException">
	/// Аналогично методу <see cref="Add{T}(T, T)"/>: может возникнуть, если операнды
	/// ссылочного типа равны <c>null</c>, а оператор вычитания не обрабатывает <c>null</c>.
	/// </exception>
	/// <exception cref="OverflowException">
	/// Может возникнуть в checked-контексте для целочисленных типов, если результат
	/// вычитания выходит за пределы диапазона типа (например, при вычитании
	/// <c>int.MinValue - 1</c>).
	/// </exception>
	/// <remarks>
	/// <para>
	/// Метод является прямой обёрткой над оператором <c>-</c>. Все особенности поведения,
	/// включая обработку отрицательных чисел, точность для чисел с плавающей запятой,
	/// переполнение и т.д., определяются типом <typeparamref name="T"/>.
	/// </para>
	/// <para>
	/// Для чисел с плавающей запятой (float, double) вычитание может приводить к потере
	/// значимости (catastrophic cancellation), если операнды близки по величине.
	/// Это не является ошибкой метода, а особенностью представления чисел с плавающей точкой.
	/// </para>
	/// <para>
	/// Примеры:
	/// <code>
	/// int diffInt = 10.Sub(4);                // 6
	/// double diffDouble = 7.5.Sub(2.2);        // 5.3
	/// decimal diffDecimal = 100.50m.Sub(30.25m); // 70.25m
	/// </code>
	/// </para>
	/// <para>
	/// Цепочки вычитаний записываются наглядно:
	/// <code>
	/// var result = total.Sub(tax).Sub(discount); // total - tax - discount
	/// </code>
	/// </para>
	/// </remarks>
	public static T Sub<T>(this T a, T b) where T : ISubtractionOperators<T, T, T>
		=> a - b;

	/// <summary>
	/// Выполняет умножение двух значений одного типа.
	/// </summary>
	/// <typeparam name="T">
	/// Тип операндов и результата. Должен реализовывать интерфейс
	/// <see cref="IMultiplyOperators{T, T, T}"/>, что гарантирует наличие оператора
	/// умножения <c>*</c>.
	/// </typeparam>
	/// <param name="a">Первый множитель. Левый операнд операции умножения.</param>
	/// <param name="b">Второй множитель. Правый операнд операции умножения.</param>
	/// <returns>
	/// Произведение значений <paramref name="a"/> и <paramref name="b"/>.
	/// Результат эквивалентен выражению <c>a * b</c>.
	/// </returns>
	/// <exception cref="ArgumentNullException">
	/// Аналогично другим методам, может возникнуть для ссылочных типов при передаче <c>null</c>,
	/// если оператор умножения не поддерживает <c>null</c>.
	/// </exception>
	/// <exception cref="OverflowException">
	/// Может возникнуть в checked-контексте для целочисленных типов при переполнении
	/// (например, <c>int.MaxValue * 2</c>). В unchecked-контексте переполнение игнорируется.
	/// </exception>
	/// <remarks>
	/// <para>
	/// Метод является прямой обёрткой над оператором <c>*</c>. Поведение полностью
	/// определяется типом <typeparamref name="T"/>.
	/// </para>
	/// <para>
	/// Для целочисленных типов следует учитывать возможность переполнения. По умолчанию
	/// контекст проверки переполнения зависит от настроек проекта. Для явного контроля
	/// можно использовать ключевые слова <c>checked</c>/<c>unchecked</c> в вызывающем коде.
	/// </para>
	/// <para>
	/// Для чисел с плавающей запятой умножение может давать результаты, не точно
	/// представимые в двоичном виде (например, 0.1 * 0.1), что является особенностью
	/// стандарта IEEE 754.
	/// </para>
	/// <para>
	/// Примеры:
	/// <code>
	/// int productInt = 6.Mul(7);               // 42
	/// double productDouble = 2.5.Mul(4.0);      // 10.0
	/// decimal productDecimal = 1.5m.Mul(2.0m);   // 3.0m
	/// 
	/// // Умножение матриц (если тип реализует оператор *)
	/// var matrix1 = new <see cref="Matrix4x4"/>(...);
	/// var matrix2 = new <see cref="Matrix4x4"/>(...);
	/// var resultMatrix = matrix1.Mul(matrix2);
	/// </code>
	/// </para>
	/// <para>
	/// Синтаксис последовательного умножения удобен при масштабировании:
	/// <code>
	/// var scaled = vector.Mul(scaleX).Mul(scaleY).Mul(scaleZ);
	/// </code>
	/// </para>
	/// </remarks>
	public static T Mul<T>(this T a, T b) where T : IMultiplyOperators<T, T, T>
		=> a * b;

	/// <summary>
	/// Выполняет деление двух значений одного типа.
	/// </summary>
	/// <typeparam name="T">
	/// Тип операндов и результата. Должен реализовывать интерфейс
	/// <see cref="IDivisionOperators{T, T, T}"/>, что гарантирует наличие оператора
	/// деления <c>/</c>.
	/// </typeparam>
	/// <param name="a">Делимое. Левый операнд операции деления.</param>
	/// <param name="b">Делитель. Правый операнд операции деления.</param>
	/// <returns>
	/// Частное от деления <paramref name="a"/> на <paramref name="b"/>.
	/// Результат эквивалентен выражению <c>a / b</c>.
	/// </returns>
	/// <exception cref="ArgumentNullException">
	/// Может возникнуть, если один из операндов ссылочного типа равен <c>null</c>, а оператор
	/// деления не обрабатывает <c>null</c>.
	/// </exception>
	/// <exception cref="DivideByZeroException">
	/// Выбрасывается, если <paramref name="b"/> равен нулю (или эквивалентному значению,
	/// вызывающему деление на ноль), а тип <typeparamref name="T"/> определяет оператор
	/// деления, который генерирует это исключение. Для целочисленных типов это всегда так.
	/// Для чисел с плавающей запятой деление на ноль даёт специальные значения
	/// (бесконечность или NaN) и не вызывает исключения.
	/// </exception>
	/// <exception cref="OverflowException">
	/// Может возникнуть в некоторых контекстах для целочисленных типов при переполнении,
	/// например, при делении <c>int.MinValue / -1</c>, так как результат (2147483648)
	/// выходит за пределы диапазона int. В checked-контексте это вызовет исключение,
	/// в unchecked — результат будет равен <c>int.MinValue</c> (из-за особенностей
	/// реализации деления в процессорах x86).
	/// </exception>
	/// <remarks>
	/// <para>
	/// Метод является прямой обёрткой над оператором <c>/</c>. Поведение деления
	/// существенно различается для разных категорий типов:
	/// <list type="bullet">
	///   <item><description>
	///     Для целочисленных типов (int, long, и т.д.) деление является целочисленным:
	///     дробная часть отбрасывается. Результат округляется в сторону нуля (truncation).
	///     Попытка деления на ноль всегда вызывает <see cref="DivideByZeroException"/>.
	///   </description></item>
	///   <item><description>
	///     Для типов с плавающей запятой (float, double) деление производится согласно
	///     стандарту IEEE 754. Деление на ноль даёт бесконечность (со знаком) или NaN
	///     при делении нуля на ноль. Исключения не генерируются.
	///   </description></item>
	///   <item><description>
	///     Для типа decimal деление выполняется с фиксированной точностью и всегда
	///     генерирует исключение <see cref="DivideByZeroException"/> при делении на ноль.
	///     Результат округляется по правилам банковского округления (round-to-even)
	///     при необходимости.
	///   </description></item>
	///   <item><description>
	///     Для пользовательских типов поведение определяется реализацией оператора.
	///   </description></item>
	/// </list>
	/// </para>
	/// <para>
	/// Важно: данный метод не выполняет никаких дополнительных проверок, таких как
	/// предотвращение деления на ноль или обработка переполнения. Вся ответственность
	/// за корректность входных данных и обработку исключений лежит на вызывающем коде.
	/// </para>
	/// <para>
	/// Примеры:
	/// <code>
	/// // Целочисленное деление
	/// int intDiv1 = 10.Div(3);        // 3 (остаток отбрасывается)
	/// int intDiv2 = 10.Div(0);        // DivideByZeroException
	/// 
	/// // Деление чисел с плавающей запятой
	/// double doubleDiv1 = 5.0.Div(2.0);   // 2.5
	/// double doubleDiv2 = 1.0.Div(0.0);    // double.PositiveInfinity
	/// double doubleDiv3 = 0.0.Div(0.0);    // double.NaN
	/// 
	/// // Decimal
	/// decimal decDiv = 10.0m.Div(3.0m);     // 3.3333333333333333333333333333m
	/// 
	/// // Векторное деление (если определено)
	/// var v = new <see cref="Vector3"/>(6, 9, 12).Div(new <see cref="Vector3"/>(2, 3, 4)); // (3, 3, 3)
	/// </code>
	/// </para>
	/// <para>
	/// Благодаря последовательному синтаксису можно легко комбинировать деление с другими
	/// операциями, например:
	/// <code>
	/// var average = sum.Div(count);               // деление
	/// var result = total.Add(tax).Div(quantity);   // (total + tax) / quantity
	/// </code>
	/// </para>
	/// </remarks>
	public static T Div<T>(this T a, T b) where T : IDivisionOperators<T, T, T>
		=> a / b;
}